    WAIT_STS(REG_1444H, 31, 1);
    WR1_PROG(REG_1420H, change_endian_long(0x00000017U));
    WR1_PROG(REG_1458H, 0x00000000U);

    HW_SCE_p_func101(0x0fe02ae6U, 0xdf675f03U, 0xe378f613U, 0x8726c14eU);
    HW_SCE_p_func043_r1();

    WR1_PROG(REG_1600H, 0x0000b4e0U);
    WR1_PROG(REG_1600H, 0x00000009U);

    WR1_PROG(REG_1444H, 0x000000a7U);
    WR1_PROG(REG_1608H, 0x800103a0U);
    WAIT_STS(REG_1444H, 31, 1);
    WR1_PROG(REG_1420H, change_endian_long(0x00000017U));
    WR1_PROG(REG_1458H, 0x00000000U);

    HW_SCE_p_func101(0x2f142308U, 0x3997921dU, 0xd2cfe1c4U, 0x855b8d17U);
    HW_SCE_p_func044_r1();

    HW_SCE_p_func100(0xf17155f0U, 0xe2ebd761U, 0x383bf86eU, 0xae85ad08U);
    WR1_PROG(REG_1608H, 0x81010000U);
    WR1_PROG(REG_1408H, 0x00005006U);
    WAIT_STS(REG_1408H, 30, 1);
    RD1_ADDR(REG_1420H, &OutData_KeyIndex[0]);

    HW_SCE_p_func100(0x79af2bfbU, 0xa10765f2U, 0xac519d06U, 0x01e93480U);
    HW_SCE_p_func103();
    WR1_PROG(REG_1444H, 0x000000a2U);
    WR1_PROG(REG_1A24H, 0x0c200104U);
    WAIT_STS(REG_1444H, 31, 1);
    WR1_PROG(REG_1420H, change_endian_long(0x00000000U));
